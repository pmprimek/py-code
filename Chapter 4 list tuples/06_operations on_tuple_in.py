# 1. Concatenation (+),Do tuples ko jodne ke liye.

t1 = (1, 2, 3)
t2 = (4, 5)
print(t1 + t2)

# 2. Repetition (*) Tuple ko multiple times repeat karne ke liye.

t = (1, 2)
print(t * 3)

# 3. Indexing Element access karne ke liye.

t = (10, 20, 30)
print(t[1])

# 4. Slicing Tuple ka part nikalne ke liye.

t = (10, 20, 30, 40, 50)
print(t[1:4])

# 5.. Membership Operators Check karne ke liye ki element tuple me hai ya nahi.

t = (10, 20, 30)
print(20 in t)
print(50 in t) # in and not in

